// Prevent us from attempting to use variables
// that are not declared
"use strict"

/*
// String data type
const myCity = "Cincinnati";
console.log(myCity);            // Output: "Cincinnati"
console.log(typeof myCity);    // Output: "string"
*/

/*
// Number data type
const myAge = 30;
console.log(myAge);            // Output: 30
console.log(typeof myAge);     // Output: "number"
*/

/*
// Boolean data type
let isStudent = true;
console.log(isStudent);        // Output: true
console.log(typeof isStudent); // Output: "boolean"
*/

/*
// Reassigning the boolean variable
isStudent = false;
console.log(isStudent);        // Output: false
console.log(typeof isStudent); // Output: "boolean"
*/

/*
// Undefined data type
let myVariable;
console.log(myVariable);        // Output: undefined
console.log(typeof myVariable); // Output: "undefined"
*/

/*
// Null data type
let emptyValue = null;
console.log(emptyValue);        // Output: null
console.log(typeof emptyValue); // Output: "object" (quirk in JavaScript)
*/

/*
// BigInt data type (ES2020+)
const bigNumber = 12345678901234567890n;
console.log(bigNumber);         // Output: 12345678901234567890n
console.log(typeof bigNumber);  // Output: "bigint"
*/

/*
// Symbol data type
const uniqueID = Symbol("id");
console.log(uniqueID);         // Output: Symbol(id)
console.log(typeof uniqueID);  // Output: "symbol"
*/

/*
// Object data type
const person = {
    name: "Dylan",
    age: 30
};
console.log(person);            // Output: { name: "Dylan", age: 30 }
console.log(typeof person);     // Output: "object"
*/

